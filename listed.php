<p><b>Listed Buildings</b></p>

<p>VAT (Value Added Tax) is normally levied at 20% on goods and services
supplied in the UK but some are exempt and some are charged with 5% or 0%.</p>

<p>&quot;Exempt&quot; and &quot;0%&quot; might seem to be the same thing but the difference is
significant in the way the tax operates.
</p>

<p>
Alterations to Listed Buildings which are dwellings or which are being converted
into a dwelling(s) are zero rated for VAT (0%).
</p>

<p>
The reason you need expert help to obtain and maximise your entitlement to 0%
VAT on alterations to your Listed building is that the VAT collectors (HM
Revenue &amp; Customs) usually assess VAT on some of the builder’s work on the
grounds that an alteration can be associated with repairing and maintaining the
building. Repairs and maintenance carry VAT at the standard rate, 20%.
</p>

<p>
This is a matter for a specialist VAT consultant, as even a good accountant may
be baffled by the rules for Listed Buildings.
</p>

<p>
Good advice and assistance with settling the amount of relief from VAT may be
less expensive than you think, and the cost will be a worthwhile investment in
peace of mind as well as for the obvious benefit of paying less tax.<br>
&nbsp;</p>

