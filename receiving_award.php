<p align="center"><font size="6">Michael J Flint - award winner !!</font></p>

<p align="center"><font size="4">Economy in Government Competition 1993</font></p>

<p>Michael's entry was an essay setting out radical proposals for streamlining,
indeed abolishing, the patchy network of VAT enquiry offices. This would be
achieved by creating a &quot;centre of excellence&quot;, with a sizeable cost
saving. Rejected at the time, if not actually ridiculed by local managers, there
is now (April 2001, under a new chairman of the Board of Customs) a national VAT
enquiry service which is a start in the right direction.</p>

<p>The runners-up award was shared with Victor Serebriakoff, then president of
Mensa, whose competition entry was a scheme to transform state pensions and to
save £1 billion in doing so.</p>

